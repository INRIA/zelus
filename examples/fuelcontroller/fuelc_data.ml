let dump2d filename (bpl1, bps1, to_dist1) (bpl2, bps2, to_dist2) table =
  let outp = open_out filename
  and f = Lookup.float_make_lookup_2d
    bpl1
    bpl2
    (Lookup.LinearInterpolation true)
    (Lookup.LinearInterpolation true)
    bps1
    bps2
    table
    (function x -> x)
  in
  (Lookup.test_2d
    (to_dist1 3 bps1)
    (function x -> x)
    (to_dist2 3 bps2)
    (function x -> x)
    outp
    f;
   close_out outp)

